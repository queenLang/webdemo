/*
* 相关命令
*
* npm init       生成package.json文件
*
* npm init -y    快速生成package.json文件
*
* npm install
*     简写 npm i
 *    安装 package.json 中 所有的依赖
 *
* npm install 包名(第三方模块)
*     简写 npm i 包名
*     安装指定的模块
*
* npm install 包名 --save
*     简写  npm i 包名 --s
*
* npm uninstall 包名
*     简写 npm un 包名
*     删除安装过的包 以及所有的依赖
*
* npm version
*     简写 npm -v
*     查看npm版本
*
* npm install -global npm
*     简写 npm i -g npm
*     更新npm版本
*
* nrm ls           列举可以使用的下载源
* nrm use + 源     更改下载源
*
*
* */

