public class Book {
    private String name, kind, price, num;

    @Override
    public String toString() {
        return "Book{" +
                "name='" + name + '\'' +
                ", kind='" + kind + '\'' +
                ", price='" + price + '\'' +
                ", num='" + num + '\'' +
                '}';
    }

    public Book() {
    }

    public Book(String name, String kind, String price, String num) {
        this.name = name;
        this.kind = kind;
        this.price = price;
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }
}
