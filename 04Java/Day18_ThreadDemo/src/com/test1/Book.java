public class Book {
    private String name ,picUrl;

    public Book() {
    }

    public Book(String name, String picUrl) {
        this.name = name;
        this.picUrl = picUrl;
    }

    @Override
    public String toString() {
        return "Book{" +
                "name='" + name + '\'' +
                ", picUrl='" + picUrl + '\'' +
                '}';
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPicUrl() {
        return picUrl;
    }

    public void setPicUrl(String picUrl) {
        this.picUrl = picUrl;
    }
}
